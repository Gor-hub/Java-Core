package homework.homework1;

public class IfForExample {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        if (a > b) {
            System.out.println("Variable a is greater than variable b");
        } else {
            System.out.println("Variable b is greater than variable b ");
        }
        for (int i = a; i <= b; i++) {
            System.out.print(i + ", ");
        }


    }
}
